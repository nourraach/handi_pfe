export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

function resolveTranslation(locale: Locale, key: string): TranslationValue | undefined {
  const parts = key.split(".");
  let current: TranslationValue | undefined = translations[locale];

  for (const part of parts) {
    if (!current || typeof current === "string") {
      return undefined;
    }
    current = current[part];
  }

  return current;
}

export function getTranslation(locale: Locale, key: string, replacements?: Record<string, string | number>): string {
  const translatedValue = resolveTranslation(locale, key);
  const fallbackValue = locale === defaultLocale ? translatedValue : resolveTranslation(defaultLocale, key);
  const resolved =
    typeof translatedValue === "string" ? translatedValue : typeof fallbackValue === "string" ? fallbackValue : key;

  if (!replacements) {
    return resolved;
  }

  return Object.entries(replacements).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    resolved,
  );
}
